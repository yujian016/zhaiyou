package com.ccc.www.bean;

public class SockBean {

	private int id;
	private String sock_name;
	private float sock_price;
	private int sock_category_id;
	private String sock_category_name;
	private String sock_log;
	private int sock_status;
	// 起批量
	private int sock_startcount;
	// 建议零售价
	private float sock_suggestprice;

	// 库存数量
	private int sock_total;
	// 单次进货需要满足的总金额
	private float order_sum_money;

	public int getSock_total() {
		return sock_total;
	}

	public void setSock_total(int sock_total) {
		this.sock_total = sock_total;
	}

	public float getOrder_sum_money() {
		return order_sum_money;
	}

	public void setOrder_sum_money(float order_sum_money) {
		this.order_sum_money = order_sum_money;
	}

	public int getSock_startcount() {
		return sock_startcount;
	}

	public void setSock_startcount(int sock_startcount) {
		this.sock_startcount = sock_startcount;
	}

	public float getSock_suggestprice() {
		return sock_suggestprice;
	}

	public void setSock_suggestprice(float sock_suggestprice) {
		this.sock_suggestprice = sock_suggestprice;
	}

	// 购买数量
	private int count;

	// 在购物车里面是否倍客户选中
	private boolean ischeck;

	public boolean isIscheck() {
		return ischeck;
	}

	public void setIscheck(boolean ischeck) {
		this.ischeck = ischeck;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public int getSock_status() {
		return sock_status;
	}

	public void setSock_status(int sock_status) {
		this.sock_status = sock_status;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getSock_name() {
		return sock_name;
	}

	public void setSock_name(String sock_name) {
		this.sock_name = sock_name;
	}

	public float getSock_price() {
		return sock_price;
	}

	public void setSock_price(float sock_price) {
		this.sock_price = sock_price;
	}

	public int getSock_category_id() {
		return sock_category_id;
	}

	public void setSock_category_id(int sock_category_id) {
		this.sock_category_id = sock_category_id;
	}

	public String getSock_category_name() {
		return sock_category_name;
	}

	public void setSock_category_name(String sock_category_name) {
		this.sock_category_name = sock_category_name;
	}

	public String getSock_log() {
		return sock_log;
	}

	public void setSock_log(String sock_log) {
		this.sock_log = sock_log;
	}

	public SockBean(int id, String sock_name, float sock_price,
			int sock_category_id, String sock_category_name, String sock_log,
			int sock_status, int count) {
		super();
		this.id = id;
		this.sock_name = sock_name;
		this.sock_price = sock_price;
		this.sock_category_id = sock_category_id;
		this.sock_category_name = sock_category_name;
		this.sock_log = sock_log;
		this.sock_status = sock_status;
		this.count = count;
	}

}
