package com.ccc.www.bean;

public class JPushMessageBean {

	int id;
	String jpshu_title;
	String jpush_context;
	int from_user_id;
	int to_user_id;
	int status;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getJpshu_title() {
		return jpshu_title;
	}

	public void setJpshu_title(String jpshu_title) {
		this.jpshu_title = jpshu_title;
	}

	public String getJpush_context() {
		return jpush_context;
	}

	public void setJpush_context(String jpush_context) {
		this.jpush_context = jpush_context;
	}

	public int getFrom_user_id() {
		return from_user_id;
	}

	public void setFrom_user_id(int from_user_id) {
		this.from_user_id = from_user_id;
	}

	public int getTo_user_id() {
		return to_user_id;
	}

	public void setTo_user_id(int to_user_id) {
		this.to_user_id = to_user_id;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public JPushMessageBean(int id, String jpshu_title, String jpush_context,
			int from_user_id, int to_user_id, int status) {
		super();
		this.id = id;
		this.jpshu_title = jpshu_title;
		this.jpush_context = jpush_context;
		this.from_user_id = from_user_id;
		this.to_user_id = to_user_id;
		this.status = status;
	}

}
