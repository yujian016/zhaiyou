package com.ccc.www.bean;

import java.io.Serializable;

public class HomeNoticeBean implements Serializable {

	int id;
	String notice_context;
	String handle_time;
	int status;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNotice_context() {
		return notice_context;
	}

	public void setNotice_context(String notice_context) {
		this.notice_context = notice_context;
	}

	public String getHandle_time() {
		return handle_time;
	}

	public void setHandle_time(String handle_time) {
		this.handle_time = handle_time;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public HomeNoticeBean(int id, String notice_context, String handle_time,
			int status) {
		super();
		this.id = id;
		this.notice_context = notice_context;
		this.handle_time = handle_time;
		this.status = status;
	}

}
