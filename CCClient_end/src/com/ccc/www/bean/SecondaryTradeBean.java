package com.ccc.www.bean;

import java.io.Serializable;

public class SecondaryTradeBean implements Serializable {

	int id;
	int user_id;
	String title;
	float price;
	String detail;
	String log1;
	String log2;
	String log3;
	String phone;
	int status;
	
	
	int  count;
	
	

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	boolean check;

	public boolean isCheck() {
		return check;
	}

	public void setCheck(boolean check) {
		this.check = check;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getUser_id() {
		return user_id;
	}

	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public float getPrice() {
		return price;
	}

	public void setPrice(float price) {
		this.price = price;
	}

	public String getDetail() {
		return detail;
	}

	public void setDetail(String detail) {
		this.detail = detail;
	}

	public String getLog1() {
		return log1;
	}

	public void setLog1(String log1) {
		this.log1 = log1;
	}

	public String getLog2() {
		return log2;
	}

	public void setLog2(String log2) {
		this.log2 = log2;
	}

	public String getLog3() {
		return log3;
	}

	public void setLog3(String log3) {
		this.log3 = log3;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public SecondaryTradeBean(int id, int user_id, String title, float price,
			String detail, String log1, String log2, String log3, String phone,
			int status) {
		super();
		this.id = id;
		this.user_id = user_id;
		this.title = title;
		this.price = price;
		this.detail = detail;
		this.log1 = log1;
		this.log2 = log2;
		this.log3 = log3;
		this.phone = phone;
		this.status = status;
	}

}
