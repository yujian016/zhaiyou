package com.ccc.www.bean;

import java.io.Serializable;

public class ScoreMarketGoodsBean implements Serializable {

	int id;
	String goods_title;
	int need_integral;
	int goods_num;
	String log;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getGoods_title() {
		return goods_title;
	}

	public void setGoods_title(String goods_title) {
		this.goods_title = goods_title;
	}

	public int getNeed_integral() {
		return need_integral;
	}

	public void setNeed_integral(int need_integral) {
		this.need_integral = need_integral;
	}

	public int getGoods_num() {
		return goods_num;
	}

	public void setGoods_num(int goods_num) {
		this.goods_num = goods_num;
	}

	public String getLog() {
		return log;
	}

	public void setLog(String log) {
		this.log = log;
	}

	public ScoreMarketGoodsBean(int id, String goods_title, int need_integral,
			int goods_num, String log) {
		super();
		this.id = id;
		this.goods_title = goods_title;
		this.need_integral = need_integral;
		this.goods_num = goods_num;
		this.log = log;
	}

}
