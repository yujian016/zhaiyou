package com.ccc.www.bean;

public class DigitalCategoryBean {
	private int id;
	private String digital_goods_category_name;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getDigital_goods_category_name() {
		return digital_goods_category_name;
	}

	public void setDigital_goods_category_name(
			String digital_goods_category_name) {
		this.digital_goods_category_name = digital_goods_category_name;
	}

	public DigitalCategoryBean(int id, String digital_goods_category_name) {
		super();
		this.id = id;
		this.digital_goods_category_name = digital_goods_category_name;
	}

}
