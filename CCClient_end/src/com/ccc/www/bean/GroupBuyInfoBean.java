package com.ccc.www.bean;

public class GroupBuyInfoBean {

	private int id;
	private String group_buy_time;
	private int status;
	
	
	public GroupBuyInfoBean(int id, String group_buy_time) {
		super();
		this.id = id;
		this.group_buy_time = group_buy_time;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getGroup_buy_time() {
		return group_buy_time;
	}
	public void setGroup_buy_time(String groupBuyTime) {
		group_buy_time = groupBuyTime;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	
	
}
