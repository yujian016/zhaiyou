package com.ccc.www.bean;

public class GroupBuyGoodsBean {

	
	private int id;
	private int group_info_id;
	private int goods_id;
	private float gb_price;
	private int gb_person_num;
	private int status;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getGroup_info_id() {
		return group_info_id;
	}
	public void setGroup_info_id(int groupInfoId) {
		group_info_id = groupInfoId;
	}
	public int getGoods_id() {
		return goods_id;
	}
	public void setGoods_id(int goodsId) {
		goods_id = goodsId;
	}
	public float getGb_price() {
		return gb_price;
	}
	public void setGb_price(float gbPrice) {
		gb_price = gbPrice;
	}
	public int getGb_person_num() {
		return gb_person_num;
	}
	public void setGb_person_num(int gbPersonNum) {
		gb_person_num = gbPersonNum;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	
	
	
	
}
